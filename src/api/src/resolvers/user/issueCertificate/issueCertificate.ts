const INFURA_API_KEY = process.env.INFURA_API_KEY
const CONTRACT_ADDRESS = process.env.CONTRACT_ADDRESS
const SIGNER_PRIVATE_KEY = process.env.SIGNER_PRIVATE_KEY

const provider: any = `https://polygon-mainnet.infura.io/v3/${INFURA_API_KEY}` || 'https://rpc.gaiaxtestnet.oceanprotocol.com:443'
const ethersProvider = new ethers.providers.JsonRpcProvider(provider);

async function estimateGas(): Promise<any> {
  let maxFeePerGas = ethers.BigNumber.from(100000000000) // fallback to 100 gwei
  // let maxPriorityFeePerGas = ethers.BigNumber.from(1000000000000) // fallback to 100 gwei
  try {
    const { data } = await axios({
      method: 'get',
      url: 'https://gasstation-mainnet.matic.network/v2'
    })
    maxFeePerGas = ethers.utils.parseUnits(
        Math.ceil(data.fast.maxFee) + '',
        'gwei'
    )
    // maxPriorityFeePerGas = ethers.utils.parseUnits(
    //   Math.ceil(data.fast.maxPriorityFee) + '',
    //   'gwei'
    // )

    return maxFeePerGas
  } catch {
  }
}


async function mintCertificate(address: string, certifcateUrl: string): Promise<any> {
  const wallet = new ethers.Wallet(SIGNER_PRIVATE_KEY || '', ethersProvider);
  const contract = new ethers.Contract(CONTRACT_ADDRESS || '', abi, ethersProvider);
  const nftContract = contract.connect(wallet);

  const overrides = {
    // gasPrice: ethers.utils.parseUnits('30', 'gwei')
    gasPrice: await estimateGas() // await ethersProvider.getGasPrice()
  };

  const tx = await nftContract.mintNFT(address, certifcateUrl, overrides);

  await tx.wait();

  return tx
}

export const setAddress: any = async (ctx: Context, next: Next): Promise<void> => {
  const issueCertificateArgs = plainToClass(IssueCertificateInputs, ctx.request.body, { excludeExtraneousValues: true })
  await validateOrReject(issueCertificateArgs, { forbidUnknownValues: true }).catch(firstError)
  const { address } = issueCertificateArgs

  const user: User = await authenticate(ctx)

  await rateLimit(user._id)

  // set user addresss
  if (!user.publicAddress || user.publicAddress === '') {
    await UserModel.updateOne({ _id: user._id }, { publicAddress: address }).exec()
  }

  ctx.status = 200
  ctx.body = {}

  await next()
}


export const issueCertificate: any = async (ctx: Context, next: Next): Promise<void> => {
  const issueCertificateArgs = plainToClass(IssueCertificateInputs, ctx.request.body, { excludeExtraneousValues: true })
  await validateOrReject(issueCertificateArgs, { forbidUnknownValues: true }).catch(firstError)
  const { coursePath } = issueCertificateArgs


  const user: User = await authenticate(ctx)
  await rateLimit(user._id)


  // check if certificate exists
  const certificate = await CertificateModel.findOne({
    coursePath,
    username: user.username,
  }).lean() as Certificate

  if (!certificate.code || certificate.code === '') {
    // issue certificate
    console.log("issuing certificate")
    const url = `https://www.chainlink.education/verify/${coursePath}/${user.username}`
    const tx = await mintCertificate(user.publicAddress || '0x69A107c851A1e1F7a7cf1914fb1eb183Fb2614F5', url)

    // set certificate 
    console.log("storing results")
    await CertificateModel.findOneAndUpdate({
      coursePath,
      username: user.username,
    }, { code: tx.hash }).lean() as Certificate
  }

  ctx.status = 200
  ctx.body = {}

  await next()
}

